package com.fem.Car;

public class Car {
	

		String name;
		int range;
		double litre;
		
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name=name;
		}
		public int getRange() {
			return range;
		}
		public void setRange(int range) {
			this.range=range;
		}
		public double getLitre() {
			return litre;
		}
		public void setLitre(double litre) {
			this.litre=litre;
		}
	}

